def fmt(n, suffix=""):
    if n is None:
        return "—"
    if isinstance(n, float):
        return f"{n:.1f}{suffix}"
    return f"{n:,}{suffix}"


def mini_sparkline(vals, color="#58a6ff", height=40, bar_width=8):
    if not vals:
        return ""
    mx = max(vals) or 1
    bars = "".join(
        f'<div class="mini-bar" style="height:{max(2, v/mx*height):.0f}px;background:{color}"></div>'
        for v in vals[-24:]
    )
    return f'<div class="mini-chart">{bars}</div>'


def sparkbar(val, max_val, color="#58a6ff"):
    pct = (val / max_val * 100) if max_val > 0 else 0
    return f'<div class="bar"><div class="bar-fill" style="width:{pct:.0f}%;background:{color}"></div></div>'


def load_performance_data() -> Dict:
    path = ROOT / "data/seo_performance.json"
    if not path.exists():
        return {"clips": [], "title_patterns": {}, "hashtag_performance": {},
                "hooks_performance": {}, "ctas_performance": {},
                "model_performance": {}, "benchmark_history": []}
    with open(path) as f:
        return json.load(f)


def load_latest_analytics() -> Optional[Dict]:
    log_dir = ROOT / "logs"
    files = sorted(log_dir.glob("analytics_*.json"), reverse=True)
    if not files:
        return None
    with open(files[0]) as f:
        return json.load(f)


def load_all_analytics() -> List[Dict]:
    log_dir = ROOT / "logs"
    files = sorted(log_dir.glob("analytics_*.json"))
    results = []
    for p in files:
        with open(p) as f:
            data = json.load(f)
            ts = p.stem.replace("analytics_", "").replace("_", "T")
            results.append({"timestamp": ts, "data": data})
    return results


def build_overview_section(pd: Dict, ad: Optional[Dict], history: List[Dict]) -> str:
    clips = pd.get("clips", [])
    total_clips = len(clips)
    unique_ids = len(set(c["clip_id"] for c in clips))
    scores = [c["performance_score"] for c in clips]
    avg_score = sum(scores) / len(scores) if scores else 0
    max_score = max(scores) if scores else 0
    min_score = min(scores) if scores else 0

    total_shorts_views = 0
    total_shorts_likes = 0
    total_shorts_comments = 0
    total_live_views = 0
    shorts_count = 0
    live_count = 0
    if ad:
        for s in ad.get("shorts", []):
            total_shorts_views += s.get("views", 0)
            total_shorts_likes += s.get("likes", 0)
            total_shorts_comments += s.get("comments", 0)
            shorts_count += 1
        for lv in ad.get("lives", []):
            total_live_views += lv.get("views", 0)
            live_count += 1

    # Trend: views per day over the analytics history
    daily_views = []
    daily_labels = []
    for h in history:
        d = h["data"]
        v = sum(s.get("views", 0) for s in d.get("shorts", []))
        daily_views.append(v)
        ts = h["timestamp"][:10]
        daily_labels.append(ts)

    view_trend = ""
    if len(daily_views) >= 2:
        trend = daily_views[-1] - daily_views[-2]
        trend_class = "up" if trend > 0 else ("down" if trend < 0 else "flat")
        trend_str = f"+{trend}" if trend > 0 else str(trend)
        view_trend = f'<span class="{trend_class}">{trend_str}</span> views vs yesterday'

    return f"""
    <h1>📊 yt-clips Analytics Dashboard</h1>
    <p class="subtitle">
      {total_clips} clips tracked · {unique_ids} unique videos · {shorts_count} shorts in latest fetch · {view_trend}
    </p>
    <div class="grid">
      <div class="card">
        <div class="label">Avg Performance Score</div>
        <div class="value">{avg_score:.3f}</div>
        <div class="delta">range: {min_score:.2f} – {max_score:.2f}</div>
        {sparkbar(avg_score, 1.0, '#58a6ff')}
      </div>
      <div class="card">
        <div class="label">Total Shorts Views</div>
        <div class="value">{fmt(total_shorts_views)}</div>
        <div class="delta">{shorts_count} shorts · avg {fmt(total_shorts_views / max(1, shorts_count))}/short</div>
        {sparkbar(total_shorts_views / max(1, shorts_count), 500, '#3fb950')}
      </div>
      <div class="card">
        <div class="label">Total Engagement</div>
        <div class="value">{fmt(total_shorts_likes + total_shorts_comments)}</div>
        <div class="delta">👍 {fmt(total_shorts_likes)} likes · 💬 {fmt(total_shorts_comments)} comments</div>
      </div>
      <div class="card">
        <div class="label">Live Stream Views</div>
        <div class="value">{fmt(total_live_views)}</div>
        <div class="delta">{live_count} streams</div>
      </div>
    </div>
    <div class="section">
      <h2>📈 Daily Shorts Views</h2>
      {mini_sparkline(daily_views, '#3fb950') if daily_views else '<div class="no-data">No historical data</div>'}
      <div style="display:flex;gap:12px;flex-wrap:wrap;font-size:12px;color:var(--muted);margin-top:4px;">
        {"".join(f'<span>{l}</span>' for l in daily_labels[-7:])}
      </div>
    </div>
    """


def build_performance_scores_section(pd: Dict) -> str:
    clips = pd.get("clips", [])
    if not clips:
        return '<div class="section"><h2>📊 Performance Scores</h2><div class="no-data">No data yet</div></div>'

    scores = [(c["performance_score"], c.get("clip_id", "?"), c.get("timestamp", "")[:10]) for c in clips]
    scores.sort(key=lambda x: -x[0])

    rows = ""
    for i, (score, cid, ts) in enumerate(scores[:20]):
        color = "#3fb950" if score >= 0.5 else ("#d29922" if score >= 0.35 else "#f85149")
        pct = score / max(s[0] for s in scores) * 100
        badge = "badge-green" if score >= 0.5 else ("badge-yellow" if score >= 0.35 else "badge-red")
        rank = f'<span class="badge {badge}">#{i+1}</span>'
        rows += f"<tr><td>{rank}</td><td style='font-family:monospace;font-size:12px'>{cid[:12]}</td><td>{ts}</td><td>{score:.4f}</td><td><div class='bar'><div class='bar-fill' style='width:{pct:.0f}%;background:{color}'></div></div></td></tr>"

    return f"""
    <div class="section">
      <h2>📊 Performance Scores</h2>
      <table>
        <tr><th>Rank</th><th>Video ID</th><th>Date</th><th>Score</th><th></th></tr>
        {rows}
      </table>
      {f'<div style="color:var(--muted);font-size:13px">… and {len(scores) - 20} more</div>' if len(scores) > 20 else ''}
    </div>
    """


def build_title_patterns_section(pd: Dict) -> str:
    tp = pd.get("title_patterns", {})
    if not tp:
        return '<div class="section"><h2>🎯 Title Patterns</h2><div class="no-data">No patterns learned yet</div></div>'

    patterns = [(k, v["avg_score"], v["count"], v["total_score"]) for k, v in tp.items()]
    patterns.sort(key=lambda x: -x[1])

    rows = ""
    for pattern, avg, count, total in patterns:
        readable = pattern.replace("_", " · ").replace(":", ": ")
        color = "#3fb950" if avg >= 0.5 else ("#d29922" if avg >= 0.35 else "#f85149")
        badge = "badge-green" if avg >= 0.5 else ("badge-yellow" if avg >= 0.35 else "badge-red")
        rows += f"""
        <tr>
          <td style="font-family:monospace;font-size:12px">{pattern[:60]}</td>
          <td><span class="badge {badge}">{avg:.3f}</span></td>
          <td>{count}x</td>
          <td><div class="bar"><div class="bar-fill" style="width:{avg*100:.0f}%;background:{color}"></div></div></td>
        </tr>"""

    best = patterns[0] if patterns else None
    worst = patterns[-1] if len(patterns) > 1 else None
    insights = ""
    if best:
        insights += f'<div style="margin-bottom:8px">✅ <strong>Best pattern:</strong> avg {best[1]:.3f} across {best[2]} clips — <code style="font-size:12px">{best[0][:60]}</code></div>'
    if worst and worst[1] < 0.4:
        insights += f'<div>❌ <strong>Worst pattern:</strong> avg {worst[1]:.3f} across {worst[2]} clips — <code style="font-size:12px">{worst[0][:60]}</code></div>'

    return f"""
    <div class="section">
      <h2>🎯 Title Patterns</h2>
      {insights}
      <table>
        <tr><th>Pattern</th><th>Avg Score</th><th>Count</th><th></th></tr>
        {rows}
      </table>
    </div>
    """



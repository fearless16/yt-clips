def build_latest_shorts_section(ad: Optional[Dict]) -> str:
    if not ad:
        return '<div class="section"><h2>🎬 Latest Shorts</h2><div class="no-data">No analytics data</div></div>'

    shorts = ad.get("shorts", [])
    if not shorts:
        return f'<div class="section"><h2>🎬 Latest Shorts</h2><div class="no-data">No shorts found in latest fetch</div></div>'

    shorts_sorted = sorted(shorts, key=lambda x: -x.get("views", 0))
    rows = ""
    for s in shorts_sorted[:15]:
        title = s.get("title", "?")[:50]
        views = s.get("views", 0)
        likes = s.get("likes", 0)
        comments = s.get("comments", 0)
        eng = ((likes + comments) / max(1, views)) * 100
        cid = s.get("id", "")[:10]
        color = "#3fb950" if views >= 200 else ("#d29922" if views >= 100 else "#f85149")
        rows += f"<tr><td style='font-family:monospace;font-size:12px'>{cid}</td><td>{title}</td><td>{fmt(views)}</td><td>{fmt(likes)}</td><td>{fmt(comments)}</td><td>{eng:.1f}%</td><td><div class='bar'><div class='bar-fill' style='width:{min(100, views/20):.0f}%;background:{color}'></div></div></td></tr>"

    return f"""
    <div class="section">
      <h2>🎬 Latest Shorts (n={len(shorts)})</h2>
      <table>
        <tr><th>ID</th><th>Title</th><th>Views</th><th>Likes</th><th>Comments</th><th>Eng Rate</th><th></th></tr>
        {rows}
      </table>
    </div>
    """


def build_top_performers_section(ad: Optional[Dict], pd: Dict) -> str:
    if not ad:
        return ""
    all_content = ad.get("shorts", []) + ad.get("videos", []) + ad.get("lives", [])
    if not all_content:
        return ""

    top = sorted(all_content, key=lambda x: -x.get("views", 0))[:5]
    rows = ""
    for i, item in enumerate(top):
        title = item.get("title", "?")[:60]
        views = item.get("views", 0)
        likes = item.get("likes", 0)
        badge = "badge-green" if i < 3 else "badge-yellow"
        rows += f"<tr><td><span class='badge {badge}'>#{i+1}</span></td><td>{title}</td><td>{fmt(views)}</td><td>{fmt(likes)}</td></tr>"

    return f"""
    <div class="section">
      <h2>🏆 Top Performers (All Content)</h2>
      <table>
        <tr><th>Rank</th><th>Title</th><th>Views</th><th>Likes</th></tr>
        {rows}
      </table>
    </div>
    """


def build_feature_importance_section(pd: Dict) -> str:
    fi = pd.get("feature_importance", {})
    if not fi:
        return ""
    rows = ""
    for feat, data in sorted(fi.items(), key=lambda x: -abs(x[1]["delta"])):
        delta = data["delta"]
        color = "#3fb950" if delta > 0 else "#f85149"
        arrow = "▲" if delta > 0 else "▼"
        label = feat.replace("has_", "").replace("_", " ").title()
        rows += f"<tr><td>{arrow} {label}</td><td style='color:{color}'>{delta:+.3f}</td><td>{data['count_with']} / {data['count_without']}</td><td style='color:{color}'>{'Positive' if delta > 0 else 'Negative'}</td></tr>"
    if not rows:
        return ""
    return f"""
    <div class="section">
      <h2>📊 Feature Importance</h2>
      <p style="color:var(--muted);font-size:13px;margin-bottom:8px">
        Score delta when feature is present vs absent (time-decayed)
      </p>
      <table>
        <tr><th>Feature</th><th>Delta</th><th>Samples (yes/no)</th><th>Impact</th></tr>
        {rows}
      </table>
    </div>
    """



def build_recommendations_section(pd: Dict) -> str:
    clips = pd.get("clips", [])
    tp = pd.get("title_patterns", {})
    hp = pd.get("hashtag_performance", {})

    recs = []

    if tp:
        patterns = [(k, v["avg_score"]) for k, v in tp.items() if v["count"] >= 2]
        patterns.sort(key=lambda x: -x[1])
        if patterns:
            best = patterns[0]
            recs.append(f"Title pattern <code>{best[0][:50]}</code> scores {best[1]:.3f} — use similar structure")
        if len(patterns) > 1:
            worst = patterns[-1]
            if worst[1] < 0.35:
                recs.append(f"Avoid title pattern <code>{worst[0][:50]}</code> (avg {worst[1]:.3f})")

    if clips:
        with_pipe = [c for c in clips if c["features"].get("has_pipe_format")]
        without_pipe = [c for c in clips if not c["features"].get("has_pipe_format")]
        if with_pipe and without_pipe:
            avg_pipe = sum(c["performance_score"] for c in with_pipe) / len(with_pipe)
            avg_no = sum(c["performance_score"] for c in without_pipe) / len(without_pipe)
            if avg_pipe > avg_no * 1.1:
                recs.append(f"Pipe format (Team vs Team | Tournament) outperforms by {((avg_pipe/avg_no)-1)*100:.0f}%")
    if hp:
        entries = [(k, sum(v)/len(v) if v else 0) for k, v in hp.items()]
        entries.sort(key=lambda x: -x[1])
        if entries:
            best_h = entries[0]
            recs.append(f"Hashtag pattern <code>{best_h[0]}</code> most effective (avg {best_h[1]:.3f})")

    if not recs:
        recs.append("More data needed for recommendations — keep publishing!")

    items = "".join(f"<li style='margin-bottom:6px'>✅ {r}</li>" for r in recs)
    return f"""
    <div class="section">
      <h2>💡 Recommendations</h2>
      <ul style="padding-left:20px;line-height:1.6">{items}</ul>
    </div>
    """


def generate_html():
    pd = load_performance_data()
    ad = load_latest_analytics()
    history = load_all_analytics()

    sections = [
        build_overview_section(pd, ad, history),
        build_top_performers_section(ad, pd),
        build_latest_shorts_section(ad),
        build_performance_scores_section(pd),
        build_feature_importance_section(pd),
        build_title_patterns_section(pd),
        build_hashtag_section(pd),
        build_model_performance_section(pd),
        build_llm_insights_section(pd),
        build_recommendations_section(pd),
    ]

    now = datetime.now().strftime("%Y-%m-%d %H:%M")
    content = "\n".join(sections)
    return HTML.format(content=content, generated_at=now)


def main():
    import argparse
    parser = argparse.ArgumentParser(description="Generate analytics HTML report")
    parser.add_argument("-o", "--output", default="reports/analytics/analytics_report.html",
                        help="Output HTML path (default: reports/analytics/analytics_report.html)")
    parser.add_argument("--open", action="store_true", help="Open in browser")
    args = parser.parse_args()

    html = generate_html()
    out_path = Path(args.output)
    out_path.parent.mkdir(exist_ok=True)
    out_path.write_text(html)
    size_kb = len(html) / 1024
    print(f"Report saved: {out_path} ({size_kb:.0f} KB)")

    if args.open:
        import subprocess, sys
        if sys.platform == "darwin":
            subprocess.run(["open", str(out_path)], check=True)
        print("Opened in browser")


